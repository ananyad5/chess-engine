board = chess.Board(chess.STARTING_FEN)
white_material = 0
black_material = 0

for square in chess.SQUARES:
    piece = board.piece_at(square)
    if not piece:
        continue
    if piece.color == chess.WHITE:
        white_material += value[piece.piece_type]
    else:
        black_material += value[piece.piece_type]

def capture(board: chess.Board, move: chess.Move) -> float:
    if board.is_en_passant(move):
        return value[chess.PAWN]
    _to = board.piece_at(move.to_square)
    _from = board.piece_at(move.from_square)
    if _to is None or _from is None:
        raise Exception(
            f"Pieces were expected at _both_ {move.to_square} and {move.from_square}"
        )
    return value[_to.piece_type] - value[_from.piece_type]